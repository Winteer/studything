package com.winter.studything.Entity;


/**
 * 成员用户实体类
 */
public class PersonInfo {

    private String serial_no;
    private String user_name;
    private String user_photo;
    private String user_code;
    private String user_cookie;
    private String user_mob;
    private String company_code;
    private String user_roles;
    private String user_post;
    private String user_flag;
    private String sort;

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getSerial_no() {
        return serial_no;
    }

    public void setSerial_no(String serial_no) {
        this.serial_no = serial_no;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_photo() {
        return user_photo;
    }

    public void setUser_photo(String user_photo) {
        this.user_photo = user_photo;
    }

    public String getUser_code() {
        return user_code;
    }

    public void setUser_code(String user_code) {
        this.user_code = user_code;
    }

    public String getUser_cookie() {
        return user_cookie;
    }

    public void setUser_cookie(String user_cookie) {
        this.user_cookie = user_cookie;
    }

    public String getUser_mob() {
        return user_mob;
    }

    public void setUser_mob(String user_mob) {
        this.user_mob = user_mob;
    }

    public String getCompany_code() {
        return company_code;
    }

    public void setCompany_code(String company_code) {
        this.company_code = company_code;
    }

    public String getUser_roles() {
        return user_roles;
    }

    public void setUser_roles(String user_roles) {
        this.user_roles = user_roles;
    }

    public String getUser_post() {
        return user_post;
    }

    public void setUser_post(String user_post) {
        this.user_post = user_post;
    }

    public String getUser_flag() {
        return user_flag;
    }

    public void setUser_flag(String user_flag) {
        this.user_flag = user_flag;
    }
}
